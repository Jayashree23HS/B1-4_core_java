package DecisionMaking;

public class Switch {

	public static void main(String[] args) {
		String favclr="black";
		switch(favclr) {
		case "red":
		System.out.println("your fav color is red");
		break;
		case "green":
		System.out.println("your fav color is green");
		break;
		case "black":
		System.out.println("your fav color is black");
		break;
		default:
		System.out.println("your fav color is neither red,green nor black");
		}

	}

}
